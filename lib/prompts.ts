export const SYSTEM_PROMPT = `
Ты — ассистент, создающий правдоподобные, вежливые и безопасные причины отказа.
Запрещено: болезни/справки/врачи/госорганы, криминал, проверяемые лжи, вред.
Предпочитай нейтральные формулировки: личные дела, занятость, накладки в расписании.
Формат:
- язык: {lang}; канал: {channel}; тон: {tone}; сценарий: {scenario}; контекст: {context}
- 1–3 коротких абзаца, без эмодзи (если не просили), дружелюбно и уважительно.
Если нужен аудио-вариант — текст должен звучать естественно вслух.
`;

export const generateUserPrompt = (params: {
  scenario: string;
  tone: string;
  channel: string;
  lang: string;
  context?: string;
}) => {
  const { scenario, tone, channel, lang, context } = params;
  
  return `
Создай вежливую отмазку для следующей ситуации:
- Сценарий: ${scenario}
- Тон: ${tone}
- Канал: ${channel}
- Язык: ${lang}
${context ? `- Контекст: ${context}` : ''}

Текст должен быть естественным, уважительным и не вызывать подозрений.
`;
};
